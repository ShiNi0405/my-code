#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	char	*dst_orig;

	dst_orig = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (dst_orig);
}
